CREATE TABLE COMPANY(
    NAME CHARACTER VARYING(50) UNIQUE NOT NULL,
    REGNUM BIGINT UNIQUE NOT NULL PRIMARY KEY,
    JUR_ADRESS CHARACTER VARYING(100) NOT NULL,
    BANK_NAME CHARACTER VARYING(50) NOT NULL,
    BANK_KODS CHARACTER VARYING(25) NOT NULL,
    BANK_KONT CHARACTER VARYING(100) NOT NULL
);




CREATE TABLE EMBROIDERY(
    ID SERIAL PRIMARY KEY NOT NULL,
    NAME CHARACTER VARYING(50) UNIQUE NOT NULL,
    HEIGHT REAL NOT NULL,
    WIDTH REAL NOT NULL,
    PHOTO_LINK TEXT,
    REL_PATH TEXT,
    TIME NUMERIC
);

